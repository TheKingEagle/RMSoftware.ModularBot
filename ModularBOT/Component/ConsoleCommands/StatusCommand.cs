using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using ModularBOT.Entity;
using Discord;
using Discord.Net;
using ModularBOT.Component;
using Microsoft.Extensions.DependencyInjection;
namespace ModularBOT.Component.ConsoleCommands
{
    public class StatusCommand : ConsoleCommand
    {
        public StatusCommand()
        {
            CommandName = "status";
        }
        public override bool Execute(string consoleInput, ref bool ShutdownCalled, ref bool RestartRequested, ref bool InputCanceled, ref DiscordNET discordNET, ref ConsoleIO console)
        {
            string status = consoleInput.Remove(0, CommandName.Length).Trim();
            console.WriteEntry(new LogMessage(LogSeverity.Info, "Client", "client status changed."));
            discordNET.Client.SetGameAsync(status);
            return true;
            //return base.Execute(consoleInput, ref ShutdownCalled, ref RestartRequested, ref InputCanceled, ref discordNET);
        }
    }
}
